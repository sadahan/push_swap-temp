#include <stdlib.h>
#include <string.h>
#include "./includes/libft.h"

void	*ft_memalloc(size_t size)
{
	void *new;

	if ((new = malloc(size)) == 0)
		return (NULL);
	ft_bzero(new, size);
	return (new);
}
