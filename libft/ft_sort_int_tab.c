#include "./includes/libft.h"

void	ft_sort_int_tab(int *tab, int tab_size)
{
	int		i;
	int		j;
	int		temp;

	i = 0;
	while (i < tab_size - 1)
	{
		j = i + 1;
		while (j < tab_size)
		{
			if (tab[i] > tab[j])
			{
				temp = tab[i];
				tab[i] = tab[j];
				tab[j] = temp;
			}
			j++;
		}
		i++;
	}
}
